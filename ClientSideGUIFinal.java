    public void addHexListeners() {
        for (int i = 0; i < allHexs.length; i++) {
        	onHex = i;
        	if (allHexs[i] == null) { return; }
            allHexs[i].addMouseListener(new MouseListener() {

            	int thisHex = onHex;
            	
				@Override
				public void mouseClicked(MouseEvent e) {
					if (e.getClickCount() == 2 & !e.isConsumed()) {
						Request theRequest = new Request();
						theRequest.setRequest(11);
						theRequest.setHexagonSelected(thisHex);
						sendRequest(theRequest);
					}
					
				}

				@Override
				public void mouseEntered(MouseEvent arg0) {
					// TODO Show all players which have this slot
					
				}

				@Override
				public void mouseExited(MouseEvent arg0) {
					// TODO Stop showing all players who have this slot
					
				}

				@Override
				public void mousePressed(MouseEvent arg0) {	}

				@Override
				public void mouseReleased(MouseEvent arg0) { }
                
            });
        }
    }
    
    public void updateResourceNumber(int[] numOfEachCard) {
    	for (int i = 0; i < allStatsLabel.length; i++) {
    		System.out.println("Updating " + i + " and original text was " + allStatsLabel[i].getText());
    		allStatsLabel[i].setText("" + numOfEachCard[i]);
    	}
    	repaint();
    }
    
