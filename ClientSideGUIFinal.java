	public BufferedImage image; 

	public PaintImage() {
		image = null;
	}

	public PaintImage (String imageName) 
	{ 
		super(); 
		try 
		{                
			image = ImageIO.read(new File(imageName)); 
		} 
		catch (IOException e) 
		{ 
			System.out.println("Fail.");
			//Not handled.
		}
	}

